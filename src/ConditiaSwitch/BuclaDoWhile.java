package ConditiaSwitch;

public class BuclaDoWhile {
    static void main(String[] args) {
        int n = 6;

        /* do se va executa oricum daca este fals macar o singura data.*/
        do {
            System.out.println(n);
            n++;   // Plus o unitate

        }
        while (n < 5);
    }
}
